const mongoose = require("mongoose");
const bcrypt = require("bcryptjs");

const jsonSchema = new mongoose.Schema(
  {
    first_name: {
      type: String,
      trim: true,
    },
    last_name: {
      type: String,
      trim: true,
    },
    is_active: {
      type: Boolean,
      default: true,
    },
  },
  {
    timestamps: true,
    versionKey: false,
  }
);

const json = mongoose.model("json", jsonSchema);
module.exports = json;
